<p class="header">Cover Letter Generator - How to use</p>
<ol>
	<li>
		Type or paste your letter text into the text editor, wrapping the words you want to replace in
		curly braces. <p>
			E.g. I am thrilled at the prospect of being a {`{role name}`} at {`{company name}`}.
		</p>
	</li>
	<li>
		Type the replacement word in the textbox that appears under each word under Fields to replace.
	</li>
	<li>
		Once you're done formatting your letter and adding replacement words, use the buttons below the
		editor to either download the rendered letter as a PDF or copy the rendered text into your
		clipboard to paste anywhere.
	</li>
	<li>
		For now, you should save your dynamic cover letter by copying the formatted letter from the
		editor (with the curly braces) and pasting it elsewhere to save it. Paste the text back into the
		editor next time you want to render a new cover letter from your template. This way, you format
		your letter once and only fill in the replacement text words next time.
	</li>
</ol>

